public class Animals {
  public String animalName;
  public String ownerName;
  public int age;
  public String breed;
  public String sex;

  public Animals(String name, String owner, int aAge, String type, String gender){
    animalName = name;
    ownerName = owner;
    age = aAge;
    breed = type;
    sex = gender;
  }
}
