public class Animals {
  public String animalName;
  public String ownerName;
  public int age;
  public String breed;
  public String sex;
  public boolean aCute;

  public Animals(String name, String owner, int aAge, String type, String gender, boolean cute){
    animalName = name;
    ownerName = owner;
    age = aAge;
    breed = type;
    sex = gender;
    aCute = cute;
  }


}
